
public class Sub {

    private String meat;
    private String bread;
    private String[] salad;
    private String Cheese;
    private boolean isToasted;
    private String sauce;



    public Sub(String meat, String bread, String[] salad, String cheese, boolean isToasted, String sauce) {
        this.meat = meat;
        this.bread = bread;
        this.salad = salad;
        Cheese = cheese;
        this.isToasted = isToasted;
        this.sauce = sauce;
    }
}


