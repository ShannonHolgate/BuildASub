public class Sub {
    private String meat;
    private String bread;
    private Boolean salad;
    private String cheese;
    private Boolean isToasted;
    private String sauce;

    public Sub(String meat, String bread, Boolean salad, String cheese, Boolean isToasted, String sauce) {
        this.meat = meat;
        this.bread = bread;
        this.salad = salad;
        this.cheese = cheese;
        this.isToasted = isToasted;
        this.sauce = sauce;
    }
}
